/// 写死的键位表
///
/// Base 层和 FN 层的按键网格映射。
/// 每个条目是一个 PC 键盘按键的显示名（如 `!1` 表示键帽上印着 `!` 和 `1`），
/// 实际输出的是基础键值（`!1` → `1` 键码），Shift 上档由 OS 处理。

use std::collections::HashMap;

/// Base 层键位表（左网格 15 键 + 右网格 15 键）
/// 按行优先排列，每行 5 个格子，共 30 格
///
/// 双字符表示键帽上的上下两档字符，仅用于显示。
pub const BASE_LAYOUT: [[&str; 10]; 3] = [
    ["q", "w", "e", "r", "t",  "y", "u", "i", "o", "p"],
    ["a", "s", "d", "f", "g",  "h", "j", "k", "l", ":;"],
    ["z", "x", "c", "v", "b",  "n", "m", ",<", ".>", "/?"],
];

/// FN 层键位表
pub const FN_LAYOUT: [[&str; 10]; 3] = [
    ["!1", "@2", "#3", "$4", "%5",  "^6", "&7", "*8", "(9", ")0"],
    ["F1", "F2", "F3", "F4", "F5",  "~`", " ",  "_-", "\"'", "+="],
    ["F6", "F7", "F8", "F9", "F10", "F11", "F12", "{[", "}]", "|\\"],
];

